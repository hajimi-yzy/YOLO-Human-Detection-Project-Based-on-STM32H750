static void Debug_CleanDCacheLine(volatile uint32_t *addr)
{
  if ((SCB->CCR & SCB_CCR_DC_Msk) != 0U) {
    uint32_t start = ((uint32_t)addr) & ~31U;
    SCB_CleanDCache_by_Addr((uint32_t *)start, 32);
  }
}

static void Debug_RecordFault(uint32_t stage)
{
  g_dbg_stage = stage;
  g_dbg_fault_cfsr = SCB->CFSR;
  g_dbg_fault_hfsr = SCB->HFSR;
  g_dbg_fault_dfsr = SCB->DFSR;
  g_dbg_fault_afsr = SCB->AFSR;
  g_dbg_fault_bfar = SCB->BFAR;
  g_dbg_fault_mmfar = SCB->MMFAR;
  g_dbg_fault_shcsr = SCB->SHCSR;
  Debug_CleanDCacheLine(&g_dbg_stage);
  Debug_CleanDCacheLine(&g_dbg_fault_cfsr);
  Debug_CleanDCacheLine(&g_dbg_fault_hfsr);
  Debug_CleanDCacheLine(&g_dbg_fault_dfsr);
  Debug_CleanDCacheLine(&g_dbg_fault_afsr);
  Debug_CleanDCacheLine(&g_dbg_fault_bfar);
  Debug_CleanDCacheLine(&g_dbg_fault_mmfar);
  Debug_CleanDCacheLine(&g_dbg_fault_shcsr);
  __DSB();
  __ISB();
}

